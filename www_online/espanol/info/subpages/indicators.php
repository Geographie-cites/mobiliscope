<section>
<h3>Global</h3>
		<p>
			<ul>
				<li><b>Población total</b>. Este indicador permite visualizar el conjunto de la población presente (16 años y más; 15 años y más para las ciudades de Quebec).</li>
				
				<li><b>Residente en/fuera del sector</b>. La población presente se ha dividido en dos categorías en función de la localización de su domicilio: personas residentes en el sector y personas residente fuera del sector.</li>
			</ul>
		</p>
</section>


<section>
<h3>Perfil demográfico</h3>
		<p>
			<ul>
				<li><b>Edad</b> en cuatro grupos: 16-24 años (15-24 para las ciudades de Quebec); 25-34 años; 35-64 años; 65 años y más.</li>

				<li><b>Sexo</b>: mujeres y hombres.</li>
				
			</ul>
		</p>

		<p>
			<ul>
				<li><b>Composición del hogar</b></li> 
				Este indicador define a qué tipo de hogar pertenecen las personas:</br>
				<ul style="margin-top: 0px">	
					<li>Para las <b>ciudades latinoamericanas</b>, la composición del hogar del individuo se clasifica en cinco grupos: persona sola; familia sin niño (<i>i.e.</i> miembros con vínculo de parentesco filial directo sin hijos menores de 16 años); hogar compuesto sin niño (<i>i.e.</i> al menos un miembro sin vínculo de parentesco filial directo sin hijos menores de 16 años); familia con niño (<i>i.e.</i> miembros con vínculo de parentesco filial directo con al menos un hijo menor de 16 años); hogar compuesto con niño (<i>i.e.</i> por lo menos un miembro sin vínculo de parentesco filial directo con al menos un hijo menor de 16 años).</li>

					<li>Para las <b>ciudades francesas</b>, la composición del hogar del individuo se clasifica en cuatro grupos: persona sola; pareja sin niño (referente del hogar o cónyuge); hogar (excepto pareja) sin niño (<i>i.e.</i> miembros de un hogar compuesto por adultos sin hijos menores de 16 años); hogar con niño (<i>i.e.</i> miembros de un hogar que consta de uno o más adultos con niños menores de 16 años; no todos los miembros están necesariamente relacionados).</li>

					<li>Para las <b>ciudades canadienses</b>, la composición del hogar del individuo se clasifica en tres grupos: persona sola; hogar sin niño (<i>i.e.</i> miembros de un hogar compuesto por adultos sin niños menores de 15 años); hogar con niño (<i>i.e.</i> miembros de un hogar compuesto por uno o más adultos con niños menores de 15 años; no todos los miembros están necesariamente emparentados entre sí).</li>
				</ul>
			</ul>	
		</p>
</section>

	
<section>
<h3>Perfil social</h3>
		<p>
		Los indicadores que caracterizan el perfil social de las personas varían según las ciudades y los países.</br>
		</p>

		<p>
		Para las <b>ciudades francesas</b>
			<ul>

				<li><b>Nivel educativo</b> a partir del último título obtenido por los encuestado·a·s. Se han integrado los niveles educativos <b>personales</b> en cuatro grupos: bajo (<i>collège</i> o menos); intermedio (secundaria, enseñanzas medias sin <i>Baccalauréat</i>); alto (hasta 2 años más después del <i>Baccalauréat</i>); y muy alto (3 años y más después del <i>Baccalauréat</i>).</br> El nivel educativo también se ha medido con respecto al <b>hogar</b> a partir del nivel educativo inferior de los adultos del hogar.</li>

				<li><b>Categoría socioprofesional (CSP)</b>. La población se ha dividido en cinco categorías según la actividad de las personas: inactivo·a·s (desempleado·a·s de larga duración, amo·a·s de casa); obrero·a·s; empleado·a·s; categorías intermedias (profesiones intermedias, artesano·a·s, comerciantes y empleadores de más de diez empleado·a·s, explotadores agrícolas); directivo·a·s y profesionales.
				</br>
				Se presenta además la categoría socioprofesional global del <b>hogar</b> a partir de la categoría inferior de los adultos del hogar.</li>

				<li><b>Ocupación principal</b> en cinco grupos: inactivo·a; jubilado·a; sin empleo; estudiante; activo·a.</li>

				<li><b>Ingresos</b> del hogar <i>- para Île-de-France únicamente</i>. Los ingresos mensuales se ajustan en función del número de adultos y de niños del hogar (ingresos por unidad de consumo - UC). Se distinguen cuatro clases de ingresos mensuales: bajos (<1.084€/UC); medios, franja inferior (1.084-1.806€/UC); medios, franja superior (1.806-2.890€/UC); altos (>2.890€/UC). Estos intervalos se han definido según el promedio de ingresos en Île-de-France (igual a 1.806€/UC en 2010). El primer umbral (1.084€/UC) o "umbral de pobreza" corresponde al 60% del promedio de ingresos; el segundo (1.806€/UC) corresponde al promedio de ingresos; el tercero (2.890€/UC) corresponde al 160% del promedio de ingresos.</li>
			</ul>
		</p>

		<p>
			Para las <b>ciudades canadienses</b>
			<ul>

				<li><b>Ingresos</b> anuales del hogar. Los ingresos anuales (después de impuestos) se ajustan en función del número de adultos y de niños del hogar. Se distinguen cuatro clases de ingresos mensuales: bajos (<19.669$/año); medios, franja inferior (19.669-39.337$/año); medios, franja superior (39.337-68.840$/año); altos (>68.840$/año). Estos intervalos se han definido según el promedio de ingresos en Quebec para el hogar de una persona (igual a 39.337$/año en 2015). El primer umbral (19.669$/año) o "medida de ingresos bajos" (MFR) corresponde al 50% del promedio de los ingresos, el segundo (39.337$/año) corresponde al promedio de los ingresos, el tercero (68.840$/año) corresponde al 175% del promedio de los ingresos. Dado el importante vacío de información en cuanto a los ingresos (20%), se ha distinguido una quinta clase específica para los ingresos faltantes. </li>

				<li><b>Ocupación principal</b> en cinco grupos: inactivo·a; jubilado·a; sin empleo; estudiante; activo·a.</li>
			</ul>
		</p>

		<p>
			Para las <b>ciudades latinoamericanas</b>
			<ul>
				
				<li><b>Nivel educativo</b> a partir del último título obtenido por los encuestado·a·s. Se han integrado los niveles educativos <b>personales</b> en cuatro grupos: bajo (menos de 9 años de estudios); intermedio (entre 9 y 11 años de estudios); alto (entre 12 y 15 años de estudios); y muy alto (al menos 16 años de estudios).</br> El nivel educativo también se ha medido con respecto al <b>hogar</b> a partir del nivel educativo superior de los adultos del hogar.</li>

				<li><b>Ingresos</b> mensuales del hogar. Los ingresos mensuales se ajustan en función del número de adultos y de niños del hogar. Se distinguen cinco clases de ingresos: muy bajos; bajos; medios; altos; muy altos. Los intervalos elegidos para cada ciudad han sido definidos según los niveles de salarios mínimos nacionales. Para los datos faltantes se han empleado métodos de imputación.</li>

				<li><b>Categoría socio-ocupacional (CSO)</b>. La población <b>activa</b> se ha sido dividido en cuatro categorías: trabajadores no cualificados; trabajadores cualificados; independientes (por cuenta propia, empresario); directivo·a·s y profesionales.</br>

				<li><b>Ocupación principal</b> en cinco grupos: inactivo·a; jubilado·a; sin empleo; estudiante; activo·a.</li>

				<li><b>Informalidad laboral </b> <i>- para Bogotá y São Paulo</i>. El trabajo informal se aborda combinando la categoría de empleo ocupado y el sector económico de la empresa empleadora inspirándonos en <a href="https://www.ilo.org/wcmsp5/groups/public/---dgreports/---dcomm/---publ/documents/publication/wcms_222986.pdf" target="_blank">métodos propuestos por la Organización Internacional del Trabajo (OIT)</a>. Cuando la información era insuficiente para seguir las reglas de decisión preconizadas por la OIT, también se han movilizado variables como el nivel educativo y de ingresos de los individuos activos encuestados.</li>
			</ul>
		</p>

</section>

<section>
<h3>Perfil residencial</h3>
		<p>
			Los indicadores relativos al perfil residencial de los personaos varían igualmente según las ciudades y los países.</br>
		</p>

		<p>
			Para las <b>ciudades francesas</b>

			<ul>
				<li><b>Residencia por zonificación en 'aires urbaines' </b>. Se distinguen tres tipos de sectores residenciales a partir de la zonificación en áreas urbanas (ZAU, 2010): centro urbano; zona urbana; zona periférica. El centro urbano hace referencia al municipio central a cargo de la encuesta origen-destino. La zona urbana corresponde a los sectores que pertenecen a un polo urbano (grande, medio o pequeño). La zona periférica reagrupa los sectores restantes (ya estén situados en el anillo periurbano de los polos o compuestos de municipios multipolarizados o aislados). Las encuestas de Carcassonne y de Besançon no cuentan con sectores pertenecientes a una zona urbana (salvo el centro urbano).</li>
				
				<li>Residencia dentro/fuera <b>Barrios Prioritarios</b> en Políticas Urbanas. La población se dividió en dos grupos según su zona fina (ZF) de residencia y la geografía prioritaria en Políticas Urbanas: residente de/fuera un barrio prioritario. Como la información sobre la residencia -o no- en un barrios prioritarios no existía en las encuestas, fue reconstruida <i>a posteriori</i> por el equipo de Mobiliscope a partir de las Zonas Finas de residencia (la malla más fina de la que disponemos). Elegimos definir como residentes de un barrios prioritarios a los encuestados cuya \"Zona Fina\" de residencia contenía una mayoría (> 51%) de habitantes de barrios prioritarios según los datos del censo de 2013. Los barrios prioritarios están presentes en todas las ciudades francesas incluidas en Mobiliscope, excepto Annecy y su región.</li>
				
				<li><b>Departamentos de residencia</b><i> - sólo para Paris y su región.</i> </li>
				
			</ul>
		</p>

		<p>
			Para las <b>ciudades latinoamericanas</b>

			<ul>
				<li><b>Anillo de residencia</b>. Los sectores de residencia se han dividido en cuatro grupos: centro; pericentro; periferia próxima; periferia alejada. Esta distribución se inspira en la propuesta del libro<a href="https://publicaciones.uexternado.edu.co/gpd-movilidades-y-cambio-urbano-bogota-santiago-y-sao-paulo-9789587724332.html" target="_blank"><em> Movilidades y cambio urbano. Bogotá, Santiago et São Paulo</em></a> dirigido por F. Dureau, T. Lulle, S. Souchaud y Y. Contreras (2015).</li>

				<li><b>Estrato socioeconómico</b> de residencia <i>- para Bogotá únicamente</i>. La estratificación socioeconómica es una <a href='https://datosabiertos.bogota.gov.co/dataset/manzana-estratificacion-bogota-d-c' target='_blank'>tipología</a> de la acción pública colombiana, cuyo objetivo es establecer las tarifas de los servicios públicos aplicadas a los hogares según el lugar de residencia, a partir de una clasificación de las viviendas en función de sus características y de su entorno próximo (calidad de la construcción, de las vías públicas, presencia de equipamientos, etc.).</li>

				<li><b>Tenencia de la vivienda</b>. Se atribuye cruzando la tenencia de la vivienda (propiedad, alquiler, usufructo, etc.) y la relación del encuestado·a con el referente del hogar. Se definen tres tipos de tenencia: propietario·a (cuando el encuestado·a -o su cónyuge- es propietario de la vivienda); arrendatario·a (cuando el encuestado·a -o su cónyuge- alquila la vivienda); persona albergada u otro estado (cuando el encuestado·a -o su cónyuge- vive en una vivienda sin ser propietario ni arrendatario). Se incluyen en esta última categoría los usufructuarios, las personas que ocupan una vivienda de forma gratuita o que se alojan en una vivienda ocupada por el propietario o por el arrendatario. Para los encuestado·a·s menores de 25 años que viven con sus padres, se tiene en cuenta la situación de tenencia de sus padres.</li>


				</li>
			</ul>
		</p>
</section>

<section>
<h3>Actividad/Modo de transporte</h3>
		<p>
			Contrariamente a los indicadores relativos a las características de los personas y de sus hogares, los indicadores referidos a las actividades y modos de transporte pueden cambiar para una persona a lo largo del día.</br>
			
			<ul>
				<li><b>Lugares de actividad</b>. Esta información se obtiene a partir de los motivos del viaje. Hemos aislado las actividades realizadas en casa e identificado 4 tipos de actividades realizadas fuera de casa: trabajo, estudios, compras y recreo (actividades recreativas, culturales o deportivas, visitas a familiares). <i>En Francia y en Canadá</i>, las "actividades no especificadas" y las "actividades de acompañamiento" han sido excluidas por su escasa frecuencia como para ser analizadas aparte, y por su excesiva especificidad como para ser reagrupadas con otros tipos de actividades. <i>En América Latina</i>, se ha introducido una otra categoría para reagrupar los trámites administrativos y los trámites calificados como "personales" (entre ellos las actividades de acompañamiento) que representan una parte importante de los desplazamientos.</li>
				
				<li>Último <b>modo de transporte</b> utilizado. Se distinguen tres modos de transporte principales: modo suave (caminar, bicicleta, etc.); vehículo motorizado individual (automóvil o motocicleta personal, taxi, etc.); transporte colectivo (autobús, metro, tren, tranvía, transporte de empresa, etc.). Para <i>Bogotá</i>, el <i>TransMilenio</i> y su red de alimentadores se han aislado de los transportes colectivos para hacer un grupo aparte.</br>
				Se trata del modo de transporte principal utilizado. Si varios modos de transporte han sido utilizados para un mismo viaje, el modo principal de transporte se define en este orden de prioridad: 1) transporte colectivo; 2) vehículo motorizado individual y 3) modo suave. Así, un viaje en el que un individuo haya utilizado el transporte colectivo y su propio automóvil se clasificará como un viaje realizado principalmente en transporte colectivo.</li>
			
			</ul>
		</p>

