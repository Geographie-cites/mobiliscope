		<section>
		<h3>Ciencia abierta</h3>

			<p>
			El desarrollo de Mobiliscope se inscribe en los <b>principios FAIR</b>: hacemos todo lo posible para garantizar que los datos construidos por el equipo y mostrados en la herramienta sean «Fáciles de encontrar, Accesibles, Interoperables y Reutilizables».</br>
			</p>

			<p>
			<b>Datos</b> mostrados en la herramienta, así como el <b>código</b> fuente para el procesamiento de los datos iniciales y la creación de la interfaz de geovisualización son <a href="/es/info/open/license">abiertos</a>. Se pueden descargar y reutilizar libremente.
			</p>

			<p>
			Mobiliscope está en constante <a href="/es/info/open/evolution">evolución</a>. Se añaden regularmente nuevas ciudades y funciones.
			</p>

			<p>
			</br>
			Si utiliza Mobiliscope o si desea participar en su desarrollo, comuníquenoslo (mobiliscope[at]parisgeo.cnrs.fr).
