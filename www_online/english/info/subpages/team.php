		<h3>Members</h3>

        <p>
    		<b><a href = "https://geographie-cites.cnrs.fr/en/members/julie-vallee/" target="_blank" >Julie Vallée</a></b> is full-time researcher in geography at the French National Center for Scientific Research (CNRS).</br>
    		She is conducting research focusing on spatial mechanisms of social inequalities in urban areas. From people's daily mobility, she aims to explore neighbourhood effects (notably on health inequalities) and to analyse the 'daycourse' of place.</br>
    		Julie is the scientific coordinator of the Mobiliscope.
    	</p>

    	<p>
    		<b><a  target="_blank" href = "https://geographie-cites.cnrs.fr/en/members/aurelie-douet/">Aurélie Douet</a></b> is an engineer in cartography and geomatics.</br> Aurélie joined the team in 2018. She had extended the Mobiliscope, originally designed for the Paris region, to some others French city regions in which origin-destination surveys are available.</br>
    		Aurélie is in charge of the new developments in the geovozualisation platform.</br>
    	</p>


        <p>
    		<b><a href = "https://www.ined.fr/en/research/researchers/Le+Roux+Guillaume" target="_blank">Guillaume Le Roux</a></b> is full-time researcher at the French Institute for Demographic Studies (INED).</br>
    		His research focuses on the relationships between social mobility, spatial mobility and recomposition of territories under urban influence. Guillaume has critically contributed to the exploration of origin-destination survey database (EGT 2010, Paris region) in order to analyse social segregation around the clock.
		</p>

        <p>
    		<b><a target="_blank" href = "https://geographie-cites.cnrs.fr/en/members/hadrien-commenges/">Hadrien Commenges</a></b> is an associate professor at the University Paris 1 Panthéon-Sorbonne. </br>
    		His research focuses on quantitative methods applied to urban geography, mainly in the field of urban mobility and transportation.
    	</p>

    	</section>

