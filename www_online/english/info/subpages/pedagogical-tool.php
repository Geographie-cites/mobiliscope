    	Students of various ages can use the Mobiliscope to discover the cities and the daily mobility of their inhabitants.

		<ul>
			<li> In <b>elementary  and middle schools</b> the Mobiliscope has proved to be a fun medium to let students explore the different types of trips (pendular, punctual and regular) and urban morphology.</br>To make the experience more playful students were asked to answer a small quiz using tablets in which they can explore their cities and their neighbourhoods.</li>
			<li> In <b>high school</b> also the Mobiliscope is beginning to be used in relation with various thematics such as mobility, segregation or urban functions.</li>
		</ul>
