</section>

<section>
  <p>
    In the Mobiliscope, people can be characterised according to their demographic and social profile and their residential area, as well as the nature of their activity and the last mode of travel they used to reach their destination.
  </p>

</section>

<section>
<h3>Global overview</h3>
    <p>
      <ul>
        <li><b>Whole population</b>. It displays the whole ambient population (aged 16 years and over; for Canadian cities aged 15 and over).</li>
        
        <li><b>Residing in/out the district</b>. The whole ambient population has been divided into two groups (residing in the district <i>versus</i> residing outside the district)</li>
      </ul>
    </p>
</section>

<section>
<h3>Demographic profile</h3>

  <p>
    <ul>
      <li><b>Age</b> divided into four groups: 16-24 years (15-24 for Canadian cities); 25-34 years; 35-64 years; 65 and more.</span></li>

      <li><b>Sex</b>: Female and Male.</li>
    </ul>
  </p>

  <p> 
    <ul>
      <li><b>Household composition</b></li>
      This indicator defines the household type in which people live: </br>
      <ul style="margin-top: 0px">  
        
        <li>For <b>French cities</b>, household composition is categorised into four groups: single person; couple without children (referent person of the household or his/her partner); household (excluding couple) without children (<i>i.e.</i> members of a household composed only of adults, without children under 16); household with children (<i>i.e.</i> members of a household composed of one or more adults with child(ren) under 16 - members are not necessarily related to each other).</li>

        <li>For <b>Canadian cities</b>, household composition is categorised into three groups: single person; household without children (<i>i.e.</i> members of a household composed of adults only, with no children under 15); household with children (<i>i.e.</i> members of a household composed of one or more adults with child(ren) under 15 - members are not necessarily related to each other).</li>

        <li>For <b>Latin American cities</b>, household composition is categorised into five groups: single person; nuclear family without children (<i>i.e.</i> members with direct filial relationship without children under 16); complex household without children (<i>i.e.</i> at least one member without a direct filial relationship and without child under 16); nuclear family with children (<i>i.e.</i> members with a direct filial relationship with at least one child under 16); complex household with children (<i>i.e.</i> at least one member without a direct filial relationship and with at least one child under 16).</li>
      </ul>
    </ul>
  </p>

</section>

  <section>
  <h3>Social profile</h3>

  <p>Indicators relative to the social profile are different from one city to another.</p>

  <p>In <b>French cities</b>
    <ul>

      <li><b>Educational level </b>. We merged respondent's achieved level of education in four groups of <b>individual </b>educational status: low (middle school or less); middle-low (high school without Baccalauréat); middle-high (up to two years after Baccalauréat); and high (three years or more after Baccalauréat).<br>
      We also computed <b>household</b> educational status corresponding to the lowest level of education of the adults in the household.</li>

      <li><b>Socioprofessional status</b>. We merged socioprofessional data in five groups of <b>individual</b> socioprofessional status: inactive (unemployed long term, housework); workers; employees; intermediate occupations (intermediary professionals, craftsmen, merchants, employers of more than 10 employees and farm operators); and managers and higher intellectual professionals.</br>
      We also computed <b>household</b> socioprofessional status corresponding to the lowest socioprofessional category of the adults in the household.</li>

      <li><b>Occupational status</b> in five groups: inactive; retired; unemployed; student; active.</li>

      <li><b>Household income</b> <i>- only for the Paris region</i>. Monthly household income has been divided by the number of adults and children in the household to get household income per consumption units (CU). We distinguished four groups: low (< 1,084€/CU); middle-low (1,084-1,806€/CU); middle-high (1,806-2,890€/CU); high (>2,890€/CU). Income intervals have been defined according to median household income in the Paris region (equal to 1,806€/CU in 2010). The first threshold (1,084€/CU) or 'poverty threshold' corresponds to 60% of median household income; the second one (1806€/CU) corresponds to the median household income; the third one (2,890€/CU) corresponds to 160% of median household income.<br></li>
    </ul>
  </p>

  <p>In <b>Canadian cities</b>
    <ul>
      
      <li><b>Household income</b>. Annual household income (after tax) has been divided by the number of adults and children in the household. We distinguished four groups: low (< 19,669$/year); middle-low (19,669-39,337$/year); middle-high (39337-68840$/year); high (>68,840$/year). Income intervals have been defined according to median household income for a one-person household in Quebec (equal to 39337$/year in 2015). The first threshold (19669$/year) or 'low income measure' corresponds to 50% of median household income; the second one (39,337$/year) corresponds to the median household income; the third one (68,840$/year) corresponds to 175% of median household income. Because of large number of missing incomes (20%), an additional 'missing' class has been added</li>

      <li><b>Occupational status</b> in five groups: inactive; retired; unemployed; student; active.</li>

    </ul>
  </p>

  <p>
      In <b>Latin American cities</b>
      <ul>

        <li><b>Level of education</b> based on the last degree obtained by the respondents. We aggregated <b>individual</b> education levels into four groups: low (less than 9 years of studies); intermediate (between 9 and 11 years of studies); high (between 12 and 15 years of studies); and very high (at least 16 years of studies).</br> Education level was also measured at the <b>household</b> level based on the highest level of education of adults in the household.</li>

        <li><b>Household income</b>. Income has been divided by the number of adults and children in the household. We distinguished five groups: very low; low; intermediate; high; very high. The ranges chosen for each city were defined according to the national minimum salary. Imputation methods were used for missing data.</li>

        <li><b>Socioprofessional status</b>. The <b>active</b> population was divided into four categories: unskilled workers; skilled workers; self-employed ; and executives and higher intellectual professions.</br>

        
        <li><b>Occupational status</b> into five groups: inactive; retired; unemployed; student; active.</li>

        
        <li><b>Professional informality</b> <i>for Bogotá and São Paulo only</i>. Informal work was approached by combining the category of employment and the economic sector of the employing firm, following the <a href="https://www.ilo.org/wcmsp5/groups/public/---dgreports/---dcomm/---publ/documents/publication/wcms_222986.pdf" target="_blank">methods proposed by the International Labor Organization (ILO)</a>. When information was insufficient to follow the decision rules advocated by the ILO, education and income level have also been used. </li>

      </ul>
    </p>

  </section>

  <section>
  <h3>Residential area</h3>

    <p>
      Indicators about the profile of residential areas are available in French and Latine America cities.</br>
    </p>

    <p>
      In <b>French cities</b>

      <ul>
        <li>
          Residential location in the <b>urban/peripheral rings</b>. Three areas were defined: inner city ; urban area ; suburban/peripheral area. 'Inner city' refers to central (administrative) city in charge of the origin-destination survey. 'Urban area' refers to districts which are totally (or mainly) included in large, middle or small urban clusters as defined in the 2010 'Zoning into Urban Areas - ZAU' (French National Institute of Statistics and Economic Studies). The remaining districts have been labelled as belonging to 'suburban/peripheral area'.
        </li>

        <li>
          Residential location in/outside <b>'Poverty Areas'</b> (<i>Quartiers Prioritaires en Politique de la Ville - QPV </i>). Respondents have been divided into two groups (living in or outside 'Poverty Areas') according to the overlap between their local area of residence (called 'zone fine' in the French survey) and the boundaries of the 'Poverty Areas' (Quartiers prioritaires de la Politique de la Ville - QPV). Since French Origin-Destination surveys do not provide information on the exact residential location, respondents have been defined as living in 'Poverty Areas' if their local areas of residence include a majority (> 51%) of population in 'Poverty Areas' according to 2013 population census. Note that there are 'Priority Areas' in every French city region in the Mobiliscope except for the Annecy region.
        </li>
        
        <li>
          <b>Departments of residence</b><i>- only for the Paris region</i>.
        </li>
      </ul>
    </p>

    <p>
      In <b>Latin American cities</b>

      <ul>
        <li>
          Residential location in the <b>urban/peripheral rings</b>. Rings were divided into 4 groups: center; pericenter; close periphery; distant periphery. This division is based on the one proposed in the book <a href="https://journals.openedition.org/lectures/18630" target="_blank"><em> Mobilities and Urban Change. Bogotá, Santiago and São Paulo</em></a> edited by F. Dureau, T. Lulle, S. Souchaud and Y. Contreras (2014).
        </li>


        <li>
          <b>Socio-economic stratum</b> of residence <i>- for Bogotá only</i>. Socioeconomic stratification is a <a href='https://datosabiertos.bogota.gov.co/dataset/manzana-estratificacion-bogota-d-c' target='_blank'>typology</a> of Colombian public actors to establish the rates of public services available for households according to their place of residence: dwellings were described according to the characteristics of the dwellings and their close environment (quality of the building, roadway, presence of facilities, etc).  
        </li>

        <li>
          <b>Housing tenure</b>. We consider the status of the household dwelling (ownership, rental, usufruct, etc.) and the place of the respondent in the household and define three tenure status: owner (when the person - or his/her partner - owns the dwelling); tenant (when the person - or his/her partner - rents the dwelling); rent-free tenants (when the person lives - or his/her partner - in a dwelling without owning or renting it). Usufructuaries, persons occupying a dwelling free of charge or housed in an owner-occupied or tenant-occupied dwelling are included in this category. For respondents under 25 living with their parents, the tenure status of the parents is considered.
        </li>

        </li>

      </ul>
    </p>
  </section>

  <section>
  <h3>Activity/Travel behaviour</h3>

  <p>In contrast with indicators related to individual/household characteristics, indicators related to <b>activity and travel mode</b> may change for one individual throughout the day.
  </br>

    <ul>
      <li><b>Activity places</b>. From trip purposes, we isolated activities performed at home and we distinguished 4 activities performed in out-of-home places: working, studying, shopping, leisure (recreational, cultural or sporting activities, family and personal visits). For <i>French and Canadian cities</i>, we have excluded 'unspecified activity' and 'activity related to chaperoning or chauffeuring'. The sample of locations related to these activities was too small and it does not make sense to gather them with some other activities. For <i>Latin American cities</i> only, we introduced another category to group together administrative and so-called "personal" activities (including accompanying activities) motivating a significant proportion of trips.</li>

      <li>Last <b>travel mode</b>. It concerns the last travel mode used to reach destination. Three groups modes have been distinguished: soft mobility (walking, cycling...), individual motor vehicle (two-wheeled motor vehicle, personal cars or motorcycles, cabs...) and collective transportation (bus, metro, train, tram, company transport...). For <i>Bogotá</i>, the <i>TransMilenio</i> and its network of secondary buses were isolated from collective transportation as a separate group.</br>
      If more than one mode of transport is used during a trip (so-called 'multi-modal' trips), the main mode is established by giving priority to collective transport, then to individual motor vehicle and finally to soft mobility. Thus, a trip in which bus and private car has been used is classified as a trip made <i>mainly</i> by collective transportation.</li>
    </ul>

  </p>

