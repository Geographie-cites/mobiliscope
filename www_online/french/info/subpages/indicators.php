<section>
	<p>
		Dans le Mobiliscope, l’utilisateur peut choisir d’afficher l’ensemble de la population présente ou bien d'observer la population présente selon son profil démographique et social, son lieu de résidence ainsi que la nature des activités réalisées et le mode de transport utilisé pour se rendre à destination.
	</p>
	</section>

<section>
<h3>Global</h3>
		<p>
			<ul>
				<li><b>Population totale</b>. Cet indicateur permet d'afficher l'ensemble de la population présente (16 ans et plus&nbsp;; 15 ans et plus pour les villes québécoises).</li>
				
				<li><b>Résidant dans/hors du secteur</b>. La population présente a été divisée en deux catégories en fonction de la localisation de leur domicile&nbsp: personnes résidentes et personnes non-résidentes du secteur</li>
			</ul>
		</p>
</section>


<section>
<h3>Profil démographique</h3>
		<p>
			<ul>
				<li><b>Âge</b> en quatre groupes&nbsp;: 16-24 ans (15-24 ans pour les villes canadiennes)&nbsp;; 25-34 ans&nbsp;; 35-64 ans&nbsp;; 65 ans et plus.</li>

				<li><b>Sexe</b>&nbsp;: Femmes et hommes.</li>
			</ul>
		</p>

		<p>
			<ul>
				<li><b>Composition du ménage</b></li> 
				Cet indicateur définit le type de ménages dans lesquels les individus vivent&nbsp;:</br>
				<ul style="margin-top: 0px">	
					
					<li>Pour les <b>villes françaises</b>, la composition du ménage est catégorisée en quatre groupes&nbsp;: personne seule&nbsp;; couple sans enfant&nbsp;; ménage (hors couple) sans enfant (<i>i.e.</i> membres d'un ménage composé uniquement d'adultes, sans enfant de moins de 16 ans)&nbsp;; ménage avec enfant (<i>i.e.</i> membres d'un ménage composé d'un ou plusieurs adultes avec enfant(s) de moins de 16 ans - tous les membres n'ont pas nécessairement un lien de parenté entre eux).</li>

					<li>Pour les <b>villes canadiennes</b>, la composition du ménage est catégorisée en trois groupes&nbsp;: personne seule&nbsp;; ménage sans enfant (<i>i.e.</i> membres d'un ménage composé uniquement d'adultes, sans enfant de moins de 15 ans)&nbsp;; ménage avec enfant (<i>i.e.</i> membres d'un ménage composé d'un ou plusieurs adultes avec enfant(s) de moins de 15 ans - tous les membres n'ont pas nécessairement un lien de parenté entre eux).</li>

					<li>Pour les <b>villes latino-américaines</b>, la composition du ménage de l'individu est catégorisée en cinq groupes&nbsp;: personne seule&nbsp;; famille nucléaire sans enfant (<i>i.e.</i> membres avec lien de parenté filial direct sans enfant de moins de 16 ans)&nbsp;; ménage complexe sans enfant (<i>i.e.</i> au moins un membre sans lien de parenté filial direct et sans présence d'enfant de moins de 16 ans)&nbsp;; famille nucléaire avec enfant (<i>i.e.</i> membres avec lien de parenté filial direct avec au moins un enfant de moins de 16 ans)&nbsp;; ménage complexe avec enfant (<i>i.e.</i> au moins un membre sans lien de parenté filial direct et avec présence d'au moins un enfant de moins de 16 ans).</li>
				</ul>
			</ul>	
		</p>
</section>

	
<section>
<h3>Profil social</h3>
		<p>
		Les indicateurs caractérisant le profil social des individus varient selon les villes et les pays.</br>
		</p>

		<p>
		Pour les <b>villes françaises</b>
			<ul>
				<li><b>Niveau d'éducation</b> à partir du dernier diplôme obtenu par les enquêté·e·s. Nous avons agrégé les niveaux d'éducation <b>individuels</b> en quatre groupes&nbsp;: faible (collège ou moins)&nbsp;; intermédiaire (secondaire, BEP ou CAP sans Bac)&nbsp;; élevé (Bac à Bac + 2)&nbsp;; et très élevé (Bac + 3 et plus).</br> Le niveau d'éducation a aussi été mesuré au niveau du <b>ménage</b> à partir du niveau d'éducation le plus bas des adultes du ménage.</li>

				<li><b>Catégorie socioprofessionnelle (CSP)</b>. La population a été divisée en cinq catégories au niveau des <b>individus</b>&nbsp;: personnes inactives (chômage de longue durée, personnes au foyer)&nbsp;; ouvriers et ouvrières&nbsp;; employé·e·s&nbsp;; catégorie intermédiaire (professions intermédiaires, artisan·e·s, commerçant·e·s et chef·fe d'entreprise de plus de dix employé·e·s, agriculteurices exploitant·e·s)&nbsp;; et cadres et professions intellectuelles.</br>
				La catégorie socioprofessionnelle globale du <b>ménage</b> est aussi présentée à partir de la CSP la plus faible des adultes du ménage.</li>

				<li><b>Occupation principale</b> de la population en cinq groupes&nbsp;: inactive&nbsp;; retraitée&nbsp;; sans emploi&nbsp;; étudiante&nbsp;; active.</li>

				<li><b>Revenus</b> du ménage <i>- pour l'Île-de-France uniquement</i>. Les revenus mensuels sont ajustés sur le nombre d'adultes et d'enfants du ménage (revenu par unité de consommation - UC). Quatre classes de revenus mensuels sont distingués&nbsp;: faible (< 1 084€/UC)&nbsp;; intermédiaire, tranche inférieure (1 084-1 806€/UC)&nbsp;; intermédiaire, tranche supérieure (1 806-2 890€/UC)&nbsp;; élevé (> 2 890€/UC). Ces intervalles ont été définis selon le revenu médian en Île-de-France (égal à 1 806€/UC en 2010). Le premier seuil (1 084€/UC) ou "seuil de pauvreté" correspond à 60% du revenu médian&nbsp;; le second (1 806€/UC) correspond au revenu médian&nbsp;; le troisième (2 890€/UC) correspond à 160% du revenu médian.</li>
			</ul>
		</p>

		<p>
			Pour les <b>villes canadiennes</b>
			<ul>

				<li><b>Revenus</b> du ménage. Les revenus annuels (après impôts) sont ajustés sur le nombre d'adultes et d'enfants du ménage. Quatre classes de revenus annuels sont distingués&nbsp;: faible (< 19 669$/an)&nbsp;; intermédiaire, tranche inférieure (19 669-39 337$/an)&nbsp;; intermédiaire, tranche supérieure (39 337-68 840$/an)&nbsp;; élevé (>68 840$/an). Ces intervalles ont été définis selon le revenu médian au Québec pour un ménage d'une personne (égal à 39 337$/an en 2015). Le premier seuil (19 669$/an) ou "Mesure de Faible Revenu" (MFR) correspond à 50% du revenu médian, le second (39 337$/an) correspond au revenu médian, le troisième (68 840$/an) correspond à 175% du revenu médian. Compte-tenu du nombre important de revenus non renseignés (20%), une cinquième classe spécifique pour les revenus manquants a été distinguée. </li>

				<li><b>Occupation principale</b> de la population en cinq groupes&nbsp;: inactive&nbsp;; retraitée&nbsp;; sans emploi&nbsp;; étudiante&nbsp;; active.</li>

			</ul>
		</p>

		<p>
			Pour les <b>villes latino-américaines</b>
			<ul>

				<li><b>Niveau d'éducation</b> à partir du dernier diplôme obtenu par les enquêté·e·s. Nous avons agrégé les niveaux d'éducation <b>individuels</b> en quatre groupes&nbsp;: faible (moins de 9 ans d'études)&nbsp;; intermédiaire (entre 9 et 11 ans d'études)&nbsp;; élevé (entre 12 et 15 ans d'études)&nbsp;; et très élevé (au moins 16 ans d'études).</br> Le niveau d'éducation a aussi été mesuré au niveau du <b>ménage</b> à partir du niveau d'éducation le plus élevé des adultes du ménage.</li>

				<li><b>Revenus</b> du ménage. Les revenus annuels des ménages sont ajustés sur le nombre d'adultes et d'enfants du ménage. Cinq classes de revenus annuels sont distingués&nbsp;: très faible&nbsp;; faible&nbsp;; intermédiaire&nbsp;; élevé&nbsp;; très élevé. Les intervalles choisis pour chaque ville ont été définis selon les niveaux de salaires minimum nationaux. Les données manquantes ont fait l'objet de méthodes d'imputation.</li>

				<li><b>Catégorie socioprofessionnelle</b>. La population <b>active</b> a été divisée en quatre catégories&nbsp;: avec un travail non qualifié&nbsp;; avec un travail qualifié&nbsp;; indépendante (à son compte, chef·fe d’entreprise); et cadre et profession intellectuelle supérieure.</br>

				<li><b>Occupation principale</b> de la population en cinq groupes&nbsp;: inactive&nbsp;; retraitée&nbsp;; sans emploi&nbsp;; étudiante&nbsp;; active.</li>

				<li><b>Informalité professionnelle</b> <i>- pour Bogotá et São Paulo</i>. Le travail informel a été approché en combinant la catégorie d'emploi occupé et le secteur économique de l'entreprise employeuse en s'inspirant des <a href="https://www.ilo.org/wcmsp5/groups/public/---dgreports/---dcomm/---publ/documents/publication/wcms_222986.pdf" target="_blank">méthodes proposées par l'Organisation Internationale du Travail (OIT)</a>. Lorsque l'information était insuffisante pour suivre les règles de décision préconisées par l'OIT, le niveau d'éducation et de revenu de la population active enquêtée ont aussi été mobilisés. </li>
			</ul>
		</p>

</section>

<section>
<h3>Profil résidentiel</h3>
		<p>
			Des indicateurs relatifs au profil résidentiel des individus sont disponibles pour les villes françaises et d'Amérique latine.</br>
		</p>

		<p>
			Pour les <b>villes françaises</b>

			<ul>
				<li><b>Résidence selon le zonage en aires urbaines </b>. Trois types de secteurs résidentiels ont été distingués à partir du zonage en Aires urbaines (ZAU, 2010)&nbsp;: ville centre&nbsp;; zone urbaine&nbsp;; zone périphérique. La ville centre fait référence à la commune centrale en charge de l'enquête ménage déplacement. La zone urbaine correspond aux secteurs appartenant à un pôle urbain (grand, moyen ou petit). La zone périphérique regroupe les secteurs restants (qu'ils soient situés dans la couronne des pôles ou composés de communes multipolarisées ou isolées). Les enquêtes de Carcassonne et de Besançon ne comptent pas de secteurs appartenant à une zone urbaine (hors ville centre).</li>
				
				<li><b>Résidence dans/hors Quartiers Prioritaires en Politique de la Ville (QPV)</b> <i>- pour la France uniquement</i>. Ont été définis comme résidents de QPV les personnes dont la « Zone Fine » de résidence contient une majorité (> 51%) d’habitants en QPV d’après des données du recensement de 2013. L'information relative à la résidence - ou à la non-résidence - en QPV n'existant pas dans les enquêtes, elle a été reconstruite a posteriori par l'équipe du Mobiliscope à partir des zones fines de résidence (le maillage le plus fin à notre disposition). A noter qu'il n'existe pas de QPV dans le périmètre de l'enquête d'Annecy.</li>
				
				<li><b>Départements de résidence</b> <i>- pour l'Île-de-France uniquement</i>.</li>
			</ul>
		</p>

		<p>
			Pour les <b>villes d'Amérique latine</b>

			<ul>
				<li><b>Couronne de résidence</b>. Les secteurs de résidence ont été divisés en 4 groupes&nbsp;: Centre&nbsp;; Péricentre&nbsp;; Périphérie proche&nbsp;; Périphérie lointaine. Ce découpage s'inspire de celui proposé dans l'ouvrage <a href="https://journals.openedition.org/lectures/18630" target="_blank"><em> Mobilités et changement urbain. Bogotá, Santiago et São Paulo</em></a> dirigé par F. Dureau, T. Lulle, S. Souchaud et Y. Contreras (2014).</li>

				<li><b>Strate socio-économique</b> de résidence <i>- pour Bogotá uniquement</i>. La stratification socio-économique est une <a href='https://datosabiertos.bogota.gov.co/dataset/manzana-estratificacion-bogota-d-c' target='_blank'>typologie</a> de l'action publique colombienne qui vise à établir les tarifs des services publics appliqués aux ménages selon leur lieu de résidence à partir d'un classement des habitations selon les caractéristiques des logements et de leur environnement proche (qualité du bâti, de la voirie, présence d'équipements, etc.). </li>

				<li><b>Statut d'occupation du logement</b>. Il est attribué en croisant le statut du logement des ménages (propriété, location, usufruit, etc.) et la place de la personne enquêtée dans le ménage. Trois statuts d'occupation sont définis&nbsp;: propriétaire (lorsque la personne - ou son conjoint - est propriétaire du logement)&nbsp;; locataire (lorsque la personne - ou son conjoint - est locataire du logement)&nbsp;; hébergée ou autre statut (lorsque la personne vit dans un logement sans qu'elle ni son conjoint n'en soit propriétaire ou locataire). Les usufruitiers, les personnes logées gratuitement ou dans un logement occupé par son propriétaire ou son locataire font partie de cette catégorie. Pour les enquêtés de moins de 25 ans vivant avec leurs parents, on considère le statut d'occupation de leurs parents.
				</li>

			</ul>
		</p>
</section>

<section>
<h3>Activité/Mode de transport</h3>
		<p>
			Contrairement aux indicateurs relatifs aux caractéristiques des individus et de leurs ménages, les indicateurs portant sur les activités et les modes de transport peuvent changer pour un individu au cours de la journée.</br>
			
			<ul>
				<li><b>Lieux d'activité</b>. Ces informations sont issues des motifs de déplacements. Nous avons isolé les activités réalisées à domicile et identifié quatre types d'activités réalisées en dehors du domicile &nbsp;: travail, études; achats et loisirs (activités récréatives, culturelles ou sportives, visites à des proches). <i>En France et au Canada</i>, les "activités non spécifiées" et les "activités d'accompagnement" ont été exclues car trop peu fréquentes pour être analysées à part, et trop spécifiques pour être regroupées avec les autres types d'activités.</br> 
				Dans <i>les villes latino-américaines</i>, nous avons ajouté une autre catégorie regroupant les démarches administratives et les démarches dites "personnelles" (dont les activités d'accompagnement) qui représentent une part importante des déplacements ont été considérées.</li>
				
				<li>Dernier <b>mode de transport</b> utilisé. Il s'agit du mode de transport principal utilisé pour se rendre à destination. </br> 
				Les modes de transport sont regroupés en trois catégories&nbsp;: modes doux (marche à pied, vélo, etc.)&nbsp;; véhicule motorisé individuel (deux-roues à moteur, voiture personnelle, taxi etc.)&nbsp;; transports collectifs (bus, métro, train, tramway, transports d'entreprise etc.).	A <i>Bogotá</i>, le <i>TransMilenio</i> et son réseau de bus secondaires ont été isolés des transports collectifs pour en faire une catégorie à part.</br>
				Si plusieurs modes de transport sont utilisés au cours d'un même déplacement (déplacements dits 'multimodaux'), le mode <b>principal</b> est établi en donnant la priorité aux transports collectifs, puis aux véhicules motorisés individuels et enfin aux modes doux. Ainsi, un déplacement au cours duquel l’individu aurait utilisé le bus et la voiture sera classé comme un déplacement fait <i>principalement</i> en transports collectifs.</li>
			
			</ul>
		</p>

