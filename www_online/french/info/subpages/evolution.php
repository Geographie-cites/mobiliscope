	<h2>Historique des versions</h2>
	</section>

	<p>
		Le Mobiliscope est un outil en constante évolution depuis la mise en ligne de la première version en 2017.
	</p>
	</br>


	<section>
		<h3>v1. v2. [2017]</h3> 

		<p>
			Les deux premières versions de l'outil concernent uniquement <b>l’Île-de-France</b>, avec une interface en <b>anglais</b>.

			<ul>
				<li><b>v1.</b> <i> [mai 2017]</i></br>
				Cette première version permet d'explorer la population présente au cours des 24 heures de la journée selon trois indicateurs (population totale, niveau d’éducation et catégorie socioprofessionnelle du ménage) et avec deux modes de représentations de la population présente (en stock et en parts). Une carte centrale donne à voir la population présente dans les différents secteurs de la région. Un premier graphique permet de visualiser les indices de ségrégation (Duncan et Moran) pour l’ensemble de la région. Un second graphique permet de visualiser la population présente dans le secteur sélectionné sur la carte. La population étudiée est âgée de plus de 16 ans.</li>
				
				</br> 
				
				<li><b>v2.</b><i> [septembre 2017]</i></br> 
				Six nouveaux indicateurs permettant de caractériser la population présente au cours de la journée ont été ajoutés (sexe, âge, revenus, occupation, lieu de résidence et activité réalisée). Un nouveau mode de représentation cartographique (en oursins) de la population non résidente est proposé.</li>

			</ul>
		</p>
		
		</section>


		<section>
		<h3>v3. [2019-2020]</h3> 

		<p>
			La 3ème version dispose d'une <b>interface bilingue (français/anglais)</b>. De <b>nouvelles villes</b> françaises mais aussi canadiennes sont progressivement intégrées pour étendre le périmètre géographique de l'outil.

			<ul>
				<li><b>v3.1.</b> <i>[mars 2019]</i></br> 
				Ajout de <b>21 villes françaises</b> (avec un nouveau seuil d'âge de la population fixé à 16 ans et plus). Un nouvel indicateur relatif au dernier mode de transport est ajouté. </li>

				</br>

				<li><b>v3.2.</b> <i>[décembre 2019]</i></br> 
				Ajout de la ville de <b>Nîmes</b>, avec l'aide de l'Agence d’urbanisme, région nîmoise et alésienne.</li>

				</br>

				<li><b>v3.3.</b> <i>[avril 2020]</i></br> 
				Ajout de <b>6 villes canadiennes</b>, avec des indicateurs caractérisant la population présente un peu moins nombreux que pour les villes françaises (sexe, âge, revenus, occupation, activité réalisée et dernier mode de transports utilisé) et un seuil d'âge légèrement inférieur (fixé à 15 ans et plus). </li>
			
			</ul>
		</p>

		</section>

		
		<section>
		<h3>v4. [2021-2023]</h3>
			
		<p>
			La 4ème version de l'outil connaît une importante <b>refonte de son interface graphique</b>. De nouvelles villes et fonctionnalités sont aussi ajoutées.</br>
			L'accès aux données est aussi facilité&nbsp;: les utilisateurs peuvent <b>télécharger</b> les <b>données de présence horaires</b> (pondérées et agrégées par secteur) telles qu'affichées dans l'outil. Ces données sont sous <b>licence libre ODbL</b> (cf. <a href="/fr/info/open/license">ici</a>)</br>
 			
		<ul>
 			<li>
 				<b>v4.0</b><i> [avril 2021]</i>

				<ul style="margin-top: 0px">
					<li>Ajout de <b>26 nouvelles villes françaises</b> comme Rouen, Metz, Besançon, Brest, Le Havre, Poitiers et Tours mais aussi les territoires de la Martinique et de La Réunion. Envion 60% de la population française est désormais couverte par l'outil.</li>
					
					<li>Open-data. Possibilité pour l'utilisateur de <b>télécharger</b> les données de présence horaires des <b>villes françaises</b> (cf. <a href="/fr/info/open/license">ici</a>).</li>
				
					<li>Intégration de 2 nouveaux indicateurs&nbsp;: (1) résidents/non-résidents du secteur; (2) habitant (oui/non) en Quartier Prioritaire en Politique de la Ville - QPV (pour la France uniquement).</li>

					<li>Ajout d'un nouveau mode de représentation pour l'indicateur "Population totale": densité de population présente (nb. de pers par km²).</li>

					<li>Affichage possible des tuiles <i>OpenStreetMap</i> afin d'aider l’utilisateur à se repérer dans les villes.

					<li>Changement des valeurs seuils pour l'affichage de la population non-résidente (mode en oursins). Le seuil de 12 des versions précédentes (seuil à partir duquel l'information était diffusée) appliqué sur le stock brut de non-résidents et sur leurs liens entre secteur de présence et secteur de résidence est supprimé. Il est remplacé par un seuil de 6 répondants bruts qui s'applique dorénavant uniquement aux liens entre secteurs de présence et de résidence.</li>

					<li>Autres modifications&nbsp;: actualisation de données pour Valenciennes et sa région à partir de l'enquête EMC² 2019&nbsp;; élargissement de l'emprise géographique de Bordeaux via l'ajout de 19 secteurs périphériques de l'enquête 2009&nbsp;; corrections des estimations des populations présentes dans les villes canadiennes&nbsp;; suppression de l'affichage des indices de ségrégation pour les indicateurs "activités" et "mode de transports".</li>

				</ul>

			</li>

			</br>

 			<li>
 				<b>v4.1</b><i> [avril 2022]</i>

 				<ul style="margin-top: 0px">

					<li>Ajout de <b>3 nouvelles villes d'Amérique latine</b>&nbsp;: Bogotá (Colombie), Santiago (Chili) et São Paulo (Brésil), avec l'ajout d'indicateurs et couches d'habillage spécifiques associés (par exemple&nbsp;: indicateur sur le travail informel, réseau de transport du TransMilenio à Bogotá). </li>

					<li>Open-data. Possibilité pour l'utilisateur de <b>télécharger</b> les données de présence horaires des <b>villes canadiennes et latino-américaines</b> (cf. <a href="/fr/info/open/license">ici</a>).</li>

					<li>Mise en place d'une <b>interface trilingue (français, anglais, espagnol)</b>.</li>

					<li>Intégration d'un nouvel indicateur relatif à la composition du ménage pour les 58 villes/régions du Mobiliscope.</li> 

					<li>Affichage possible des images satellites et/ou aériennes afin d'aider l’utilisateur à se repérer dans les villes.</li>

					<li>Réajustement du calcul des heures de présence. Ce réajustement induit un décalage d'une heure dans les données de présence par rapport aux données affichées dans les versions antérieures de l'outil&nbsp;: la population présente observée dans un secteur à une heure <i>h</i> s'observait auparavant à <i>h-1</i>.</li>

					<li>Création de l'URL correspondant aux géovisualisations affichées. Par exemple, la proportion de femmes à Bogotá à 8h dans le secteur de La Candelaria est maintenant directement accessible via cette <a href = "/fr/geoviz/bogota?m1=2&m2=2&m3=1&m4=part&t=8&s=UTAM94" target="_blank">URL</a>. Possibilité de partager l'URL via le nouveau bouton "Partager la vue" <img src="/dist/assets/share.svg" width="20px" height= "20px"/>.</li>

					<li>Amélioration de l'outil de recherche par nom de commune <img src="/dist/assets/search-black.svg" width="20px" height= "20px"/>&nbsp;: le secteur associé à la commune recherchée est désormais automatiquement sélectionné dans la géovisualisation (sélection du secteur sur la carte et le graphique du haut).</li>

					<li>Modification de la définition du mode principal de transport pour les villes canadiennes&nbsp;: en cas de déplacements multimodaux, le mode principal de transport est à présent défini en suivant cet ordre de priorité&nbsp;: 1) Transports collectifs&nbsp;; 2) Véhicule motorisé individuel et 3) Mode doux (marche à pied, vélo, etc.). Cet ordre de priorité est dorénavant le même que celui qui est adoptée pour les villes françaises et latino-américaines.</li>
			
				</ul>
			</li>

			</br>

			<li>
 				<b>v4.2</b><i> [avril 2023]</i>
 				
 				<ul style="margin-top: 0px">
 				
 					<li>Vaste <b>réorganisation de l'architecture logicielle</b> (refactorisation du code web) afin de garantir la stabilité, l'efficacité et la pérennité de l'outil et faciliter les développements ultérieurs. </li>

 					<li><b>Refactorisation des codes R</b> de traitements des données. Les données ont aussi été <b>réorganisées et allégées</b>.</li>

 					<li>Pour les cartes choroplèthes, modification des méthodes de discrétisations désormais réalisées "à la volée" ville par ville grâce à la librairie <a href = "https://github.com/simogeo/geostats" target="_blank" >geostats.js</a>. Les nouvelles bornes de classe peuvent par conséquence différer de celles de la v4.1, en particulier pour les indicateurs "Sexe", "Zone/Couronne de résidence" et "Activité" dont les bornes de classe étaient auparavant similaires pour l'ensemble des villes.</li>
 							
 					<li>Introduction de règles plus strictes de suppression des doubles comptes de présence (<i>i.e.</i> des personnes comptées deux fois dans un même secteur à une heure donnée) afin de corriger certaines estimations de présence. Ces corrections impactent essentiellement les villes canadiennes, et très à la marge les villes françaises et latino-américaines.</li>
 							
 					<li>Simplification des titres des cartes et graphiques.</li>

 					<li>Création d'un <b><a href="https://doi.org/10.5281/zenodo.7822016" target="_blank">dépôt Zenodo</a></b> regroupant <b>toutes les données</b> proposées en open-data dans l'outil.</li> 

 				</ul>
 				
			</li>

		</ul>

	 	</p>		
		</section>


