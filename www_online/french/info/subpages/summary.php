	<section>
	<h2>En quelques mots...</h2>
	</section>

	</br>
	<section>
		<p>
		Le Mobiliscope est un outil de géovisualisation pour explorer la population présente et la mixité sociale dans les villes au cours des 24 heures de la journée. 
		</p>

		<p>
		Il permet d'explorer librement <b>58 villes et leur région</b> (49 en France, 6 au Québec, Canada et 3 en Amérique latine). En France ce sont ainsi <b>10 000 communes</b> qui sont couvertes par l'outil (60% de la population).</br>
		</p>

		<p>
		Derrière cet outil, il y a une <a href="/fr/info/about/team">équipe</a> de géographes et de géomaticiennes et plusieurs <a href="/fr/info/about/partners">partenaires</a> institutionnels.</br>
		</p>

		<p>
		L'équipe a également décrit l'outil et analysé les dynamiques temporelles, spatiales et sociales associées dans différents <a href="/fr/info/about/publications">articles et communications</a>.
		</p>

		<p class="ss-style-dots" ></p>
		</section>


		<section>
		<h3>Fonctionnement de l'outil</h3>

			<p>
			Les <a href="/fr/info/methods/data">données</a> proviennent de grandes enquêtes sur les déplacements des individus (968 000 participants et près de 2,9 millions de déplacements) réalisées entre 2009 et 2019.
			</p>

			<p>
			Les populations présentes dans les villes au cours des 24 heures d'une journée type de semaine (lundi-vendredi) sont caractérisées en fonction d'<a href="/fr/info/methods/indicators">indicateurs</a> démographiques (sexe et âge) et sociaux (niveau d'éducation, catégorie socioprofessionnelle, et parfois revenus) mais aussi en fonction des secteurs de résidence, du type d'activité réalisée et du mode de transport utilisé.
			</p>

			<p>
			La carte centrale et les deux graphiques qui s'animent au fil des heures sont les éléments clefs de la <a href="/fr/info/methods/geovizualisation">géovisualisation</a> proposée.
			</p>

			<p class="ss-style-dots" >
			 <button class="style-button mb50"><a href="/fr/geoviz/rennes/">Découvrir l'outil pour <b>Rennes</b> et sa région</a></button>
			</p>
			</section>

		<section>
		<h3>Un outil pour analyser, aménager et enseigner</h3>

			<p>
			Une grande variété de cas d'usages existe et différents publics peuvent se saisir de l'outil. Il peut ainsi être utilisé comme&nbsp;:
			<ul>
				<li> un <a href="/fr/info/multitask-tool/planning-tool">outil d'aménagement</a> par les acteurs publics et les collectivités locales,</li> 
				<li> un <a href="/fr/info/multitask-tool/scientific-tool">outil scientifique</a> pour les chercheurs,</li>
				<li> un <a href="/fr/info/multitask-tool/pedagogical-tool">outil pédagogique</a> pour les enseignants.</li>
			</ul>
			</p>

			<p>
			Mais quel que soit votre profil, vous pouvez aussi juste jouer avec le Mobiliscope pour le plaisir&nbsp;!
			</p>
		
		<p class="ss-style-dots" ></p>
		</section>

		<section>
		<h3>Science ouverte</h3>

			<p>
			Le développement du Mobiliscope s’inscrit dans les <b>principes du FAIR</b>&nbsp;: nous faisons notre maximum pour que les données construites par l'équipe et affichées dans l'outil soient « <b>F</b>aciles à trouver, <b>A</b>ccessibles, <b>I</b>nteropérables et <b>R</b>éutilisables ».</p>

			<p>
			Les <b>données</b> affichées dans l'outil ainsi que le <b>code source</b> pour traiter les données initiales et pour créer l'interface de géovisualisation sont <a href="/fr/info/open/license">librement accessibles et réutilisables</a>.
			</p>

			<p>
			Le Mobiliscope est en constante <a href="/fr/info/open/evolution">évolution</a>. De nouvelles villes et fonctionnalités sont régulièrement ajoutées.
			</p>

			<p>
			Si vous utilisez le Mobiliscope ou si vous souhaitez participer à son développement, n'hésitez pas à nous le faire savoir (mobiliscope[at]parisgeo.cnrs.fr).
			</p>

